#!/usr/bin/env bash
# DSpace 8 platform plugin for ChengetAi Deploy.
# Sourced by lib/utils.sh:load_plugin — requires DEPLOY_DIR, DEPLOY_NAME and
# the profile variables (INSTITUTION, ADMIN_EMAIL, ...) to be set.
#
# ChengetAi Deploy is an ORCHESTRATOR. The canonical DSpace deployment
# lives in the Bulawayo Polytechnic DSpace repository; this plugin never
# duplicates that logic. Per deployment it:
#
#   clone      the reference repository into deployments/<name>/engine/
#   configure  ports, instance name, admin account, generated DB secret
#              (via the environment contract the reference repo supports)
#   brand      copy deployments/<name>/branding/ over the engine assets
#   deploy     run the reference repository's install.sh
#   verify     the installer waits for the backend health endpoint
#   maintain   start/stop/status/logs/backup/restore/update/remove below

# shellcheck disable=SC2034  # PLUGIN_* consumed by the CLI after sourcing
PLUGIN_NAME="dspace"
PLUGIN_DESCRIPTION="DSpace 8 institutional repository"
PLUGIN_STATUS="available"

# The canonical DSpace deployment implementation.
ENGINE_REPO="https://github.com/wgmasvix-hue/bulawayo-polytechnic-dspace-.git"
ENGINE_BRANCH="claude/dspace-deployment-review-48qeth"

# Database credentials inside the dspacedb container
# (dspace/dspace-postgres-pgcrypto defaults; the password is generated by
# the reference installer and lives in the engine's .env, chmod 600).
DB_USER="dspace"
DB_NAME="dspace"

engine_dir() {
    echo "$DEPLOY_DIR/engine"
}

branding_dir() {
    echo "$DEPLOY_DIR/branding"
}

ui_port() {
    echo "${UI_PORT:-4000}"
}

rest_port() {
    echo "${REST_PORT:-8080}"
}

require_engine() {
    if [ ! -d "$(engine_dir)/.git" ]; then
        error "Deployment '$DEPLOY_NAME' has not been deployed yet. Run: chengetai deploy $DEPLOY_NAME"
    fi
}

# All compose operations run against the reference repository's campus
# stack, namespaced per deployment so data volumes never collide.
# (The reference stack uses fixed container names, so only one DSpace
# deployment can be RUNNING per server; volumes stay separate.)
pcompose() {
    docker compose -p "chengetai-$DEPLOY_NAME" \
        -f "$(engine_dir)/docker-compose-campus.yml" \
        --project-directory "$(engine_dir)" "$@"
}

plugin_server_ip() {
    local ip
    ip=$(grep -s '^SERVER_IP=' "$(engine_dir)/.env" | cut -d= -f2)
    echo "${ip:-localhost}"
}

plugin_urls() {
    local ip
    ip=$(plugin_server_ip)
    echo "  UI (browser):  http://${ip}:$(ui_port)"
    echo "  REST API:      http://${ip}:$(rest_port)/server"
}

# Create or reset the DSpace administrator against the already-running
# backend — no redeploy needed. Non-interactive when ADMIN_EMAIL and
# ADMIN_PASS are set, otherwise runs DSpace's own interactive prompt. This
# is what recovers from the "passwords do not match" case cleanly.
plugin_admin() {
    require_engine
    if ! container_running dspace; then
        error "The backend container (dspace) is not running. Start it first: chengetai start $DEPLOY_NAME"
    fi
    local lang="${ADMIN_LANG:-en}"
    if [ -n "${ADMIN_EMAIL:-}" ] && [ -n "${ADMIN_PASS:-}" ]; then
        info "Creating/updating administrator $ADMIN_EMAIL (non-interactive)..."
        if docker exec dspace /dspace/bin/dspace create-administrator \
                -e "$ADMIN_EMAIL" \
                -f "${ADMIN_FIRST_NAME:-Repository}" \
                -l "${ADMIN_LAST_NAME:-Admin}" \
                -c "$lang" \
                -p "$ADMIN_PASS"; then
            echo ""
            info "Administrator ready: $ADMIN_EMAIL"
            echo "  Sign in at: http://$(plugin_server_ip):$(ui_port)"
        else
            error "create-administrator failed (see the output above)."
        fi
    else
        info "Launching DSpace's interactive administrator setup..."
        echo "  Tip: set ADMIN_EMAIL and ADMIN_PASS (or pass --email/--password)"
        echo "       to run this without any prompts."
        echo ""
        docker exec -it dspace /dspace/bin/dspace create-administrator
    fi
}

# ── Institutional branding ──────────────────────────────────────────────────
# Make the repository read as the institution's own, not "DSpace". The name,
# short name, publisher and preview brand live in local.cfg (the only mounted
# config), so a `restart dspace` applies them with no rebuild; the browser tab
# title follows the site name automatically. Logo + favicon are staged into the
# deployment's branding/ and baked into the Angular image on --apply. (Colours,
# footer, login and error-page styling are compiled into the frontend image and
# need a themed source build — see docs/BRANDING.md.)

# Set (or replace, in place) a "key = value" in a DSpace .cfg. DSpace's config
# combines duplicate keys into a list rather than taking the last, so we must
# replace the existing line, never append a second one. Portable (no gawk
# gensub): the key/value are passed via the environment so any characters in an
# institution name are handled literally.
_brand_set_key() {
    local cfg="$1" key="$2" val="$3"
    BKEY="$key" BVAL="$val" awk '
      BEGIN { key=ENVIRON["BKEY"]; val=ENVIRON["BVAL"]; done=0; found=0 }
      {
        line=$0; t=line; sub(/^[ \t]+/,"",t)
        if (!done && substr(t,1,length(key))==key) {
          rest=substr(t,length(key)+1); sub(/^[ \t]*/,"",rest)
          if (substr(rest,1,1)=="=") { print key " = " val; done=1; found=1; next }
        }
        print line
      }
      END { if (!found) print key " = " val }
    ' "$cfg" > "$cfg.tmp" && mv "$cfg.tmp" "$cfg"
}

plugin_brand() {
    require_engine
    local cfg brand
    cfg="$(engine_dir)/dspace/config/local.cfg"
    brand="$(branding_dir)"
    [ -f "$cfg" ] || error "local.cfg not found for '$DEPLOY_NAME' ($cfg). Deploy first."

    local action="set" repo_name="" institution="" shortname="" publisher="" tagline=""
    local logo="" favicon="" primary="" accent="" do_apply=0
    while [ $# -gt 0 ]; do
        case "$1" in
            --name|--repository) repo_name="$2"; shift 2 ;;
            --institution)       institution="$2"; shift 2 ;;
            --shortname)         shortname="$2"; shift 2 ;;
            --publisher)         publisher="$2"; shift 2 ;;
            --tagline)           tagline="$2"; shift 2 ;;
            --logo)              logo="$2"; shift 2 ;;
            --favicon)           favicon="$2"; shift 2 ;;
            --primary)           primary="$2"; shift 2 ;;
            --accent|--secondary) accent="$2"; shift 2 ;;
            --apply)             do_apply=1; shift ;;
            --status)            action="status"; shift ;;
            *) error "Unknown option for brand: $1" ;;
        esac
    done

    mkdir -p "$brand"
    local envf="$brand/brand.env"

    if [ "$action" = "status" ]; then
        if [ -f "$envf" ]; then
            echo -e "${GREEN}✓${NC} Branding for '$DEPLOY_NAME':"
            sed 's/^/    /' "$envf"
        else
            echo -e "${YELLOW}○${NC} No branding profile yet for '$DEPLOY_NAME'."
            echo "    Set it: chengetai brand $DEPLOY_NAME --name \"DARE Digital Repository\" --institution DARE"
        fi
        [ -f "$brand/logo.png" ]    && echo "    logo    : $brand/logo.png"
        [ -f "$brand/favicon.png" ] && echo "    favicon : $brand/favicon.png"
        return 0
    fi

    # Merge previous values with flags (flags win), then persist.
    # shellcheck disable=SC1090
    [ -f "$envf" ] && source "$envf"
    [ -n "$repo_name" ]   && REPOSITORY_NAME="$repo_name"
    [ -n "$institution" ] && INSTITUTION_NAME="$institution"
    [ -n "$shortname" ]   && SHORT_NAME="$shortname"
    [ -n "$publisher" ]   && PUBLISHER="$publisher"
    [ -n "$tagline" ]     && TAGLINE="$tagline"
    if [ -n "$primary" ]; then
        [[ "$primary" =~ ^#[0-9A-Fa-f]{6}$ ]] || error "--primary must be a hex colour like #0E5C4A"
        PRIMARY_COLOR="$primary"
    fi
    if [ -n "$accent" ]; then
        [[ "$accent" =~ ^#[0-9A-Fa-f]{6}$ ]] || error "--accent must be a hex colour like #C8A24B"
        ACCENT_COLOR="$accent"
    fi

    : "${INSTITUTION_NAME:=${REPOSITORY_NAME:-Institutional Repository}}"
    : "${REPOSITORY_NAME:=$INSTITUTION_NAME}"
    if [ -z "${SHORT_NAME:-}" ]; then
        # Multi-word names → initials (Digital Archive Research Exchange → DARE);
        # a single word is kept whole (DARE → DARE, not "D").
        if [ "$(printf '%s' "$INSTITUTION_NAME" | wc -w)" -le 1 ]; then
            SHORT_NAME="$INSTITUTION_NAME"
        else
            SHORT_NAME="$(printf '%s' "$INSTITUTION_NAME" | awk '{for(i=1;i<=NF;i++) printf toupper(substr($i,1,1))}')"
        fi
    fi
    : "${PUBLISHER:=$INSTITUTION_NAME}"

    {
        echo "INSTITUTION_NAME=$(printf '%q' "$INSTITUTION_NAME")"
        echo "REPOSITORY_NAME=$(printf '%q' "$REPOSITORY_NAME")"
        echo "SHORT_NAME=$(printf '%q' "$SHORT_NAME")"
        echo "PUBLISHER=$(printf '%q' "$PUBLISHER")"
        echo "TAGLINE=$(printf '%q' "${TAGLINE:-}")"
        echo "PRIMARY_COLOR=$(printf '%q' "${PRIMARY_COLOR:-}")"
        echo "ACCENT_COLOR=$(printf '%q' "${ACCENT_COLOR:-}")"
    } > "$envf"

    # Colour palette → branding/theme.css. The engine injects and
    # runtime-mounts this over the UI, so colours apply on a frontend
    # restart (image rebuild also picks it up). Derived shades use CSS
    # color-mix(), so no colour math is needed here.
    if [ -n "${PRIMARY_COLOR:-}" ]; then
        local ac="${ACCENT_COLOR:-$PRIMARY_COLOR}"
        cat > "$brand/theme.css" <<THEME
/* Generated by ChengetAi Deploy — institutional palette for ${INSTITUTION_NAME}.
 * Loaded after the compiled DSpace styles; regenerate with: chengetai brand */
:root {
  --ct-primary: ${PRIMARY_COLOR};
  --ct-primary-dark: color-mix(in srgb, ${PRIMARY_COLOR} 75%, black);
  --ct-primary-soft: color-mix(in srgb, ${PRIMARY_COLOR} 12%, white);
  --ct-accent: ${ac};
  /* Re-point DSpace/Bootstrap theme variables at the institution palette. */
  --bs-primary: ${PRIMARY_COLOR};
  --bs-link-color: ${PRIMARY_COLOR};
  --bs-link-hover-color: var(--ct-primary-dark);
  --ds-header-bg: ${PRIMARY_COLOR};
  --ds-header-navbar-border-bottom-color: var(--ct-accent);
  --ds-navbar-bg: ${PRIMARY_COLOR};
  --ds-expandable-navbar-bg: var(--ct-primary-dark);
  --ds-footer-bg: var(--ct-primary-dark);
  --ds-breadcrumb-bg: var(--ct-primary-soft);
}
/* Component fallbacks for styles compiled against fixed colours. */
.btn-primary { background-color: var(--ct-primary); border-color: var(--ct-primary); }
.btn-primary:hover, .btn-primary:focus, .btn-primary:active {
  background-color: var(--ct-primary-dark); border-color: var(--ct-primary-dark); }
.btn-outline-primary { color: var(--ct-primary); border-color: var(--ct-primary); }
.btn-outline-primary:hover { background-color: var(--ct-primary); border-color: var(--ct-primary); color: #fff; }
a { color: var(--ct-primary); }
a:hover { color: var(--ct-primary-dark); }
.badge.bg-primary, .badge-primary { background-color: var(--ct-primary) !important; }
ds-themed-header header, ds-header header { background-color: var(--ct-primary); }
ds-themed-footer footer, ds-footer footer { background-color: var(--ct-primary-dark); }
.text-primary { color: var(--ct-primary) !important; }
.bg-primary { background-color: var(--ct-primary) !important; }
.form-control:focus { border-color: var(--ct-primary);
  box-shadow: 0 0 0 .2rem color-mix(in srgb, var(--ct-primary) 25%, transparent); }
THEME
        info "Colour theme generated: primary ${PRIMARY_COLOR}, accent ${ac}."
    fi

    [ -n "$logo" ]    && { [ -f "$logo" ] || error "Logo not found: $logo"; cp "$logo" "$brand/logo.png"; info "Logo staged."; }
    [ -n "$favicon" ] && { [ -f "$favicon" ] || error "Favicon not found: $favicon"; cp "$favicon" "$brand/favicon.png"; info "Favicon staged."; }

    # Institutional identity into local.cfg (in-place; browser title follows name).
    _brand_set_key "$cfg" "dspace.name" "$REPOSITORY_NAME"
    _brand_set_key "$cfg" "dspace.shortname" "$SHORT_NAME"
    _brand_set_key "$cfg" "webui.preview.brand" "$INSTITUTION_NAME"
    _brand_set_key "$cfg" "webui.preview.brand.abbrev" "$SHORT_NAME"
    _brand_set_key "$cfg" "crosswalk.dissemination.DataCite.publisher" "$PUBLISHER"

    echo ""
    info "Branding set for '$DEPLOY_NAME':"
    echo "    Repository : $REPOSITORY_NAME   (site name + browser tab title)"
    echo "    Institution: $INSTITUTION_NAME  ($SHORT_NAME)"
    echo "    Publisher  : $PUBLISHER"
    echo ""

    if [ "$do_apply" = 1 ]; then
        info "Applying branding..."
        apply_branding
        info "Rebuilding the frontend image (logo/favicon) — this can take a few minutes..."
        docker build -f "$(engine_dir)/Dockerfile.angular" -t bpoly-dspace-angular:latest "$(engine_dir)" >/dev/null
        if container_running dspace; then
            pcompose up -d dspace-angular
            pcompose restart dspace
        fi
        info "Applied. The repository now presents as $REPOSITORY_NAME."
    else
        echo "  Apply with: chengetai brand $DEPLOY_NAME --apply   (restarts backend; rebuilds UI if logo/favicon changed)"
        echo "  Config-only now (names/title):  chengetai restart $DEPLOY_NAME"
    fi
}

# ── ORCID integration ───────────────────────────────────────────────────────
# Enable "Sign in with ORCID" + researcher-identity linking on a running
# DSpace, or report/disable it. The campus stack bind-mounts ONLY local.cfg
# into the backend, so every setting (including the auth-stack override that
# adds OrcidAuthentication) is written there; a `restart dspace` applies it.
ORCID_MARK_BEGIN="# >>> ChengetAi ORCID (managed — do not edit between these markers) >>>"
ORCID_MARK_END="# <<< ChengetAi ORCID (managed) <<<"

# Remove the managed ORCID block from a local.cfg (idempotent, no-op if absent).
_orcid_strip() {
    local cfg="$1" tmp
    tmp="$(mktemp)"
    awk -v b="$ORCID_MARK_BEGIN" -v e="$ORCID_MARK_END" '
        $0==b {skip=1; next}
        skip && $0==e {skip=0; next}
        !skip {print}
    ' "$cfg" > "$tmp"
    cat "$tmp" > "$cfg"
    rm -f "$tmp"
}

plugin_orcid() {
    require_engine
    local cfg
    cfg="$(engine_dir)/dspace/config/local.cfg"
    [ -f "$cfg" ] || error "local.cfg not found for '$DEPLOY_NAME' ($cfg). Deploy first."

    local action="enable" env="sandbox" client_id="" restart=1
    local client_secret="${ORCID_CLIENT_SECRET:-}"
    while [ $# -gt 0 ]; do
        case "$1" in
            --client-id)         client_id="$2"; shift 2 ;;
            --client-secret)     client_secret="$2"; shift 2 ;;
            --sandbox)           env="sandbox"; shift ;;
            --production|--live) env="production"; shift ;;
            --status)            action="status"; shift ;;
            --disable|--off)     action="disable"; shift ;;
            --no-restart)        restart=0; shift ;;
            *) error "Unknown option for orcid: $1" ;;
        esac
    done

    # The redirect URI DSpace advertises; it MUST be registered with the ORCID
    # app or the OAuth handshake is rejected.
    local server_url redirect
    server_url="$(grep -sE '^dspace\.server\.url[[:space:]]*=' "$cfg" | head -1 | cut -d= -f2- | tr -d ' ')"
    redirect="${server_url%/}/api/authn/orcid"

    case "$action" in
      status)
        if grep -qF "$ORCID_MARK_BEGIN" "$cfg"; then
            local cur_env cur_id
            if grep -sE '^orcid\.domain-url' "$cfg" | grep -q sandbox; then
                cur_env="sandbox"
            else
                cur_env="production"
            fi
            cur_id="$(grep -sE '^orcid\.application-client-id' "$cfg" | tail -1 | cut -d= -f2- | tr -d ' ')"
            echo -e "${GREEN}✓${NC} ORCID is ENABLED for '$DEPLOY_NAME'"
            echo "    Environment  : $cur_env"
            echo "    Client ID    : ${cur_id:-(unset)}"
            echo "    Redirect URI : $redirect"
        else
            echo -e "${YELLOW}○${NC} ORCID is not configured for '$DEPLOY_NAME' (password login only)."
            echo "    Enable it: chengetai orcid $DEPLOY_NAME --client-id APP-XXXX --client-secret SECRET"
        fi
        return 0 ;;
      disable)
        _orcid_strip "$cfg"
        info "ORCID disabled for '$DEPLOY_NAME' (managed block removed; password login remains)."
        if [ "$restart" = 1 ] && container_running dspace; then
            info "Restarting the backend to apply..."
            pcompose restart dspace
        fi
        return 0 ;;
    esac

    # enable
    [ -n "$client_id" ] || error "ORCID needs --client-id APP-XXXX (from ORCID → Developer Tools)."
    [ -n "$client_secret" ] || error "ORCID needs --client-secret (or export ORCID_CLIENT_SECRET to keep it out of shell history)."

    local domain api public
    if [ "$env" = "production" ]; then
        domain="https://orcid.org"
        api="https://api.orcid.org/v3.0"
        public="https://pub.orcid.org/v3.0"
    else
        domain="https://sandbox.orcid.org"
        api="https://api.sandbox.orcid.org/v3.0"
        public="https://pub.sandbox.orcid.org/v3.0"
    fi

    _orcid_strip "$cfg"   # clear any previous managed block, then write fresh
    {
        echo ""
        echo "$ORCID_MARK_BEGIN"
        echo "# ORCID sign-in + researcher identity. Environment: $env"
        echo "orcid.application-client-id = $client_id"
        echo "orcid.application-client-secret = $client_secret"
        echo "orcid.domain-url = $domain"
        echo "orcid.api-url = $api"
        echo "orcid.public-url = $public"
        echo "orcid.synchronization-enabled = true"
        echo "plugin.sequence.org.dspace.authenticate.AuthenticationMethod = org.dspace.authenticate.PasswordAuthentication, org.dspace.authenticate.OrcidAuthentication"
        echo "$ORCID_MARK_END"
    } >> "$cfg"

    echo ""
    info "ORCID ($env) configured for '$DEPLOY_NAME'."
    echo ""
    echo "  ➜ Register this EXACT redirect URI in your ORCID app"
    echo "    (ORCID → Developer Tools → your application → Redirect URIs):"
    echo ""
    echo "        $redirect"
    echo ""
    echo "    Client ID : $client_id"
    echo "    Secret    : stored in $cfg"
    echo ""
    if [ "$restart" = 1 ]; then
        if container_running dspace; then
            info "Restarting the backend to apply (first boot after a config change can take a few minutes)..."
            pcompose restart dspace
            echo ""
            info "Done. Users will see 'Sign in with ORCID' at http://$(plugin_server_ip):$(ui_port)"
        else
            warn "Backend not running — start it to apply: chengetai start $DEPLOY_NAME"
        fi
    else
        info "Config written. Apply with: chengetai restart $DEPLOY_NAME"
    fi
}

# Copy this deployment's branding over the engine's assets. The engine's
# own copies are reset from git first so upstream updates never conflict
# with local branding.
apply_branding() {
    local engine brand
    engine=$(engine_dir)
    brand=$(branding_dir)

    if [ ! -d "$brand" ]; then
        mkdir -p "$brand"
        cp "$TEMPLATES_DIR/dspace/branding/"* "$brand/" 2>/dev/null || true
    fi

    git -C "$engine" checkout -- assets 2>/dev/null || true
    [ -f "$brand/logo.png" ]    && cp "$brand/logo.png"    "$engine/assets/bpoly-logo.png"
    [ -f "$brand/favicon.png" ] && cp "$brand/favicon.png" "$engine/assets/favicon.png"
    # Institution colour palette (generated by `chengetai brand --primary ...`).
    # The engine injects + runtime-mounts assets/theme.css into the UI.
    [ -f "$brand/theme.css" ]   && cp "$brand/theme.css"   "$engine/assets/theme.css"
}

plugin_deploy() {
    local engine
    engine=$(engine_dir)

    # 1. Clone (or reuse) the canonical repository. Its installer handles
    #    its own updates on re-runs.
    if [ ! -d "$engine/.git" ]; then
        if [ -e "$engine" ]; then
            error "Deployment '$DEPLOY_NAME' has a legacy engine directory. Back up its data, then: chengetai remove $DEPLOY_NAME && chengetai deploy $DEPLOY_NAME"
        fi
        info "Downloading deployment engine (canonical DSpace repository)..."
        git clone --depth 1 -b "$ENGINE_BRANCH" "$ENGINE_REPO" "$engine"
    fi

    # 2. Brand.
    apply_branding

    # 3+4. Configure & deploy: the reference installer does the real work.
    #      The profile flows in through its documented environment
    #      contract; COMPOSE_PROJECT_NAME namespaces volumes per
    #      deployment. It only prompts for whatever is missing (the
    #      admin password, unless ADMIN_PASS is set).
    UI_PORT="$(ui_port)" \
    REST_PORT="$(rest_port)" \
    DSPACE_NAME="${REPOSITORY:-$INSTITUTION}" \
    COMPOSE_PROJECT_NAME="chengetai-$DEPLOY_NAME" \
    INSTALL_DIR="$engine" \
        bash "$engine/install.sh"
}

# Point the deployment's public URL at an HTTPS domain — used by
# `chengetai domain`, which puts Caddy in front. Rewrites the frontend
# config.yml (ui + rest blocks) and the backend compose env, then rebuilds
# the frontend image and recreates the stack. Backups (.bak) are kept so the
# change is easy to roll back.
plugin_domain() {
    require_engine
    local domain="${1:-}"
    [ -n "$domain" ] || error "plugin_domain: a domain is required"

    local engine cfg compose
    engine="$(engine_dir)"
    cfg="$engine/config.yml"
    compose="$engine/docker-compose-campus.yml"

    # 1. Frontend: rewrite the ui: and rest: blocks to https://<domain>.
    if [ -f "$cfg" ]; then
        cp -f "$cfg" "$cfg.bak"
        awk -v d="$domain" '
            /^ui:/   { blk="ui";   print; next }
            /^rest:/ { blk="rest"; print; next }
            /^[^[:space:]]/ { blk="" }
            {
                if (blk=="ui" || blk=="rest") {
                    if ($1=="ssl:")  { print "  ssl: true"; next }
                    if ($1=="host:") { print "  host: " d;  next }
                    if ($1=="port:") { print "  port: 443"; next }
                }
                print
            }
        ' "$cfg.bak" > "$cfg"
        info "Frontend pointed at https://$domain (backup: config.yml.bak)"
    else
        warn "config.yml not found — skipping the frontend repoint."
    fi

    # 2. Backend: set the public server/ui URLs in the compose env.
    if [ -f "$compose" ]; then
        cp -f "$compose" "$compose.bak"
        sed -i -E "s#(dspace__P__server__P__url:).*#\1 https://$domain/server#" "$compose"
        sed -i -E "s#(dspace__P__ui__P__url:).*#\1 https://$domain#"          "$compose"
        if ! grep -q 'dspace__P__server__P__url' "$compose"; then
            warn "Backend server-URL env not found in compose — set dspace.server.url to https://$domain/server manually."
        else
            info "Backend public URL set to https://$domain/server (backup: docker-compose-campus.yml.bak)"
        fi
    else
        warn "docker-compose-campus.yml not found — skipping the backend repoint."
    fi

    # 3. Rebuild the frontend image (config.yml is baked in) and recreate.
    require_docker
    info "Rebuilding the frontend and recreating services (can take a few minutes)..."
    docker build -f "$engine/Dockerfile.angular" -t bpoly-dspace-angular:latest "$engine" >/dev/null
    pcompose up -d
    echo ""
    info "DSpace is now served at https://$domain"
}

plugin_start() {
    require_engine
    pcompose up -d
    echo ""
    info "Services started."
    echo ""
    plugin_urls
    echo ""
    echo "  The backend can take 3-5 minutes to come up."
    echo "  Check progress with: chengetai status $DEPLOY_NAME"
    echo ""
}

plugin_stop() {
    require_engine
    # Containers are removed; the assetstore, database and Solr volumes are kept.
    pcompose down --remove-orphans
    echo ""
    info "All services stopped. Data volumes are preserved."
    echo "Start again with: chengetai start $DEPLOY_NAME"
    echo ""
}

plugin_restart() {
    require_engine
    pcompose restart
    echo ""
    info "Services restarted."
    echo "The backend can take 3-5 minutes to come up. Check with: chengetai status $DEPLOY_NAME"
    echo ""
}

plugin_status() {
    require_engine
    pcompose ps

    local ip
    ip=$(plugin_server_ip)

    echo ""
    if curl -sf "http://localhost:$(rest_port)/server/api" >/dev/null 2>&1; then
        echo -e "${GREEN}✓${NC} REST API       : http://${ip}:$(rest_port)/server"
    else
        echo -e "${RED}✗${NC} REST API       : not responding (http://${ip}:$(rest_port)/server)"
    fi

    if curl -sf "http://localhost:$(ui_port)" >/dev/null 2>&1; then
        echo -e "${GREEN}✓${NC} User Interface : http://${ip}:$(ui_port)"
    else
        echo -e "${RED}✗${NC} User Interface : not responding (http://${ip}:$(ui_port))"
    fi
    echo ""
}

